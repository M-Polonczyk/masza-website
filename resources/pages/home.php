            <p>
                Nasze życie kształtują napotkani ludzie. Kiedy ktoś zadaje mi pytanie, dlaczego język rosyjski, odpowiadam, że poznałam właściwego człowieka, który wskazał mi drogę. Kiedy miałam 10 lat wiedziałam, co chcę w życiu robić. Filologię wschodniosłowiańską ukończyłam w 2005 roku. Nie spodziewałam się wtedy, że uda mi się tak dużo osiągnąć dzięki znajomości języka rosyjskiego.
                </p>
                <p>
                Centrum Języka Rosyjskiego Masza powstało w 2013 roku. Moja własna szkoła. Mały pokoik w olsztyńskim Inkubatorze Przedsiębiorczości. Zaczęłam współorganizować kolonie językowe oraz sportowe dla dzieci z Rosji i nie tylko. Byłam dyrektorem animatorem, nauczycielem języka polskiego. Czułam się spełniona i potrzebna. Poproszono mnie, abym stała się członkiem kadry pedagogicznej największej wówczas w Obwodzie Kaliningradzkim szkoły nr 56. W 2014 otrzymałam telefon z Rosyjskiego Międzynarodowego Uniwersytetu w Moskwie z propozycją podjęcia się nauczania języka polskiego oraz kultury polskiej tamtejszych studentów. Poznałam młodych, pięknych ludzi z Rosji, Kazachstanu, Azerbejdżanu, Ukrainy, Białorusi, którzy z pasją uczyli się naszego języka i studiowali kulturę Polski. Rektor poprosił mnie, abym uczyła tamtejszych wykładowców. Tworzyły się kolejne grupy, otrzymywałam telefony z propozycją pracy w szkołach językowych w Rosji. W tym samym czasie rozpoczęłam współpracę z domem dziecka w Zielenogradsku. Rok później zostałam wykładowcą na jeszcze jednym uniwersytecie w Kaliningradzie, Korporacyjnym. Byłam dumna z siebie i swojej pracy.
                </p>
                <p>
                Z uwagi na brak zadowalających mnie materiałów do nauki języka polskiego jako obcego, rozpoczęłam pracę na własnym podręcznikiem. Nigdy go nie wydałam, ale pracuję na swoim skrypcie do dziś. W 2016 roku ukończyłam Szkołę im. Oliega Andriejewa w Moskwie i zostałam trenerem szybkiego czytania, uczenia się oraz technik zapamiętywania. Prowadziłam kursy na terenie Rosji oraz Polski. Nigdy nie mówiłam swoim rosyjskim słuchaczom, że jestem obcokrajowcem, bo by się obrazili, że Polka uczy ich, jak mają czytać w swoim ojczystym języku. Masza w Polsce też rozwijała się w najlepsze, przenieśliśmy się do większego lokalu. Było nas już troje: Masza z Rosji, Alina z Ukrainy i ja. W tym samym roku zajęłam się tłumaczeniami analityków giełdowych. Jestem współtwórcą strony internetowej o notowaniach giełdowych, inwestycjach finansowych. W roku 2018 dostałam propozycję pracy w Szkole Mistrzostwa Sportowego, przyjęłam ją z radością. Uczyłam młodych sportowców rosyjskiego. Wybuchła pandemia, życie przewróciło się do góry nogami. A w naszym Centrum drzwi się nie zamykały. Z naszymi „przyjaciółmi” (tak nazywamy uczniów CJMasza) organizowaliśmy różne spotkania. Lepiliśmy pielmieni, oglądaliśmy filmy, graliśmy w gry planszowe, robiliśmy oliwie, winiegriet, słuchaliśmy muzyki, organizowaliśmy Stary Nowy Rok, podróżowaliśmy do Czarnobyla, Mongolii, Kazachstanu… Masza stała się drugim domem dla wielu ludzi. Pandemia nie przeszkodziła nam w organizowaniu kolonii dla dzieci. Było trudniej, ale nie ma przeszkód nie do pokonania.
                </p>
                <p>
                Zrobiło się ciasno, przenieśliśmy się do lokalu z czterema salami lekcyjnymi. Naszym
                nowym lektorem została Dasza z Syberii. Uczyliśmy rosyjskiego, ukraińskiego, angielskiego oraz
                polskiego. Wykorzystaliśmy popularność naszego Centrum i braliśmy czynny udział w WOŚP,
                organizowaliśmy zbiórki schroniskom dla zwierząt. Nasze uczennice zajęły 1 i 2 miejsce w
                Międzynarodowej Olimpiadzie Języka Rosyjskiego, organizowanej przez Uniwersytet w Petersburgu
                oraz Ministerstwo Edukacji Federacji Rosyjskiej. Dziewczyny pokonały ponad 9500 osób z całego
                świata! W kolejnych latach powtarzają sukces. W 2021 roku zorganizowaliśmy I Ogólnopolski Konkurs
                MASZAmbicje. W 2022 roku wybucha wojna. II konkursu już nie będzie. Jadę na granicę z Ukrainą,
                pomagam w ośrodkach recepcyjnych, organizuję transport kobiet, dzieci i zwierząt, w Maszy brakuje
                miejsca na przynoszone przez naszych przyjaciół dary. Ukraińcy są wdzięczni mi, że rozmawiam po
                rosyjsku, tak łatwiej przekazać emocje. Uczymy uchodźców polskiego, podejmujemy współpracę z
                Caritasem, Bankiem Żywności, cerkwią grekokatolicką w Olsztynie, pomagamy zorganizować
                Ukraińcom życie w Polsce. Uczymy języka polskiego w ośrodkach dla uchodźców, takich jak Rybaki
                czy Kuźnia Społeczna. Współpracujemy z psychologami i Prokuraturą Rejonową w Olsztynie w
                zakresie tłumaczeń – zeznań uchodźców. Do czasu, kiedy wybuchła wojna myślałam, że życie Maszy
                będzie dzielić się na to przed i po pandemii. Językiem rosyjskim posługuje się mało Polaków w tej
                chwili, więc pracownicy i uczniowie Maszy pomagają uchodźcom u lekarza, dentysty, weterynarza.
                Pomagamy znaleźć mieszkanie, pracę, uczestniczymy w kontaktach z urzędami. Dziś wiem, że to
                wojna w Ukrainie zmieniła nas najbardziej. Nie martwię się o język rosyjski. Ludzie pytają mnie, czy
                rosyjski zostanie usunięty ze szkół. Oczywiście, że nie, odpowiadam za każdym razem. W lutym 2022
                roku z logo znika słowo „rosyjskiego” i pojawia się ukraińska flaga. Już nie jesteśmy CJRMasza. Jest mi
                bardzo przykro, bronię siebie i CJMaszy przed nienawiścią całego świata. Poznaję fascynujących ludzi
                z Ukrainy, rodzą się nowe przyjaźnie i nowe pomysły. Języka rosyjskiego zaczynają się uczyć dentyści,
                lekarze, radcy prawni. Bo wojna w Ukrainie to nie wojna przeciwko językowi rosyjskiemu. I rozumieją
                to również Ukraińcy. Życie w Maszy wre. Na nasze spotkania wciąż przychodzą Ukraińcy, Rosjanie,
                Białorusini, Kazasi i Mołdawianie. Rozmawiamy wszyscy po rosyjsku. W tym roku nawiązałam
                współpracę z żydowską fundacją Midrasha z Izraela. Zorganizowałam rosyjskojęzycznym dzieciom z
                Izraela, Szkocji, USA, Ukrainy, Austrii, Francji i Niemiec kolonię letnią. Były to dwa turnusy obozów o
